  def radio_button_render
    str = ""
    [ 'Yes Please!', 'NO', 'Pick-Up Only (city)', 'Delivery Only (city)'].each do |theme|
      str << "<br>"
      str << (radio_button_tag 'theme', theme, theme).to_s
      str << '&nbsp;&nbsp;' + (theme.humanize).to_s
    end
    str.html_safe
  end

  def valid_to_send?(elms)
    #trust the user and UI that required if filled out
    is_valid = true
    %W[ name phone email furniture services ].each do |val|
      if elms[val].blank?
        is_valid = false
        break
      end
    end
    return is_valid
  end

